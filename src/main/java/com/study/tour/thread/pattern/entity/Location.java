package com.study.tour.thread.pattern.entity;

public final class Location {

    public final double x;

    public final double y;

    public Location(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
